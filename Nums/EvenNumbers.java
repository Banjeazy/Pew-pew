public class EvenNumbers {

    public static int arrayEvenNums() {
        int[] nums = new int[100];
        int i = 0;
        for(int index = 0;index < nums.length; index++);{
            if(index % 2 == 0 && index != 22){
                i++;
                if(nums[index] % 2 == 0){
                    i++;
                }
            }
        }


    }
        
    }

